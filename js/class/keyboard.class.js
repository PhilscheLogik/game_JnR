/** DE
 * Repräsentiert den Zustand der Tastatur.
 * Speichert, welche Tasten gedrückt sind.
 */
/** EN
 * Represents the state of the keyboard.
 * Stores which keys are pressed.
 */
class Keyboard {
  /** DE
  * Gibt an, ob die linke Pfeiltaste/A Taste gedrückt ist.
  * @type {boolean}
  */
  /** EN
  * Indicates whether the left arrow/ A key is pressed.
  * @type {boolean}
  */
  LEFT = false;

  /** DE
  * Gibt an, ob die rechte Pfeiltaste/ D Taste gedrückt ist.
  * @type {boolean}
  */
  /** EN
  * Indicates whether the right arrow/ D key is pressed.
  * @type {boolean}
  */
  RIGHT = false;

  /** DE
  * Gibt an, ob die obere Pfeiltaste/W Taste gedrückt ist.
  * @type {boolean}
  */
  /** EN
  * Indicates whether the top arrow/W key is pressed.
  * @type {boolean}
  */
  UP = false;

  /** DE
  * Gibt an, ob die untere Pfeiltaste/S Taste gedrückt ist.
  * @type {boolean}
  */
  /** EN
  * Indicates whether the bottom arrow/S key is pressed.
  * @type {boolean}
  */
  DOWN = false;

  /** DE
  * Gibt an, ob die Q Taste gedrückt ist.
  * @type {boolean}
  */
  /** EN
  * Indicates whether the Q key is pressed.
  * @type {boolean}
  */
  Q = false;

  /** DE
  * Gibt an, ob die E Taste gedrückt ist.
  * @type {boolean}
  */
  /** EN
  * Indicates whether the E key is pressed.
  * @type {boolean}
  */
  E = false;

  /** DE
  * Gibt an, ob die Zeit aktiv ist (long Idle).
  * @type {boolean}
  */
  /** EN
  * Indicates whether the time is active (long Idle).
  * @type {boolean}
  */
  TIME = true;
}
